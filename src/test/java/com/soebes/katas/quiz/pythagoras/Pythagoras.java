package com.soebes.katas.quiz.pythagoras;

public class Pythagoras {
	
	private int a;
	private int b;
	private int c;
	public Pythagoras(int a, int b, int c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	@Override
	public String toString() {
		return "Pythagoras [a=" + a + ", b=" + b + ", c=" + c + "]";
	}
	
} 
